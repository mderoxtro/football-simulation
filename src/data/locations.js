let selectCollege = () => {
  let allColleges = []
  for(let location of locations){
    for(let college of location.colleges) allColleges.push(college)
  }
  return allColleges[Math.floor(Math.random() * allColleges.length)]
}
  
let selectTown = () => {
  let total = 0
  for(let loc of locations){
    total += loc.weight
  }
  let threshold = Math.random() * total
  total = 0
  for(let i = 0; i < locations.length; i++){
    total += locations[i].weight
    if(total >= threshold){
      let chosenLocation = locations[i]
      let returnLocation = {
        state: chosenLocation.stateName,
        stateShort: chosenLocation.stateAbbr,
        city: chosenLocation.cities[Math.floor(Math.random() * chosenLocation.cities.length)]
      }
      return returnLocation
    }
  }
}
  
